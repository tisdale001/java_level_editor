
public class LevelData {
    public int numRows;
    public int numCols;
    public String tileSetFolderName;

    public LevelData(int numRows, int numCols, String tileSetFolderName) {
        this.numRows = numRows;
        this.numCols = numCols;
        this.tileSetFolderName = tileSetFolderName;
    }
}